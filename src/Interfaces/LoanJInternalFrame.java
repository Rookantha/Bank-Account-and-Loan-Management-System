        model.setRowCount(0);
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/bankdb?zeroDateTimeBehavior=convertToNull", "root", "");
            PreparedStatement select = conn.prepareStatement("SELECT * FROM loan");
            ResultSet result = select.executeQuery();
            ResultSetMetaData rsmd = result.getMetaData();
            int cols = rsmd.getColumnCount();
            while(result.next()) {
                Object[] obj = new Object[cols];
                for(int i=0;i<cols;i++) {
                    obj[i]=result.getObject(i+1);
                }
                model.addRow(obj);
            }
        } catch (Exception e) {
            System.out.println(e);
        } 
